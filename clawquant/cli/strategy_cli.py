import typer

strategy_app = typer.Typer(
    help="Strategy management commands",
    no_args_is_help=True,
)


# ---------------------------------------------------------------------------
# list
# ---------------------------------------------------------------------------
@strategy_app.command("list")
def strategy_list(
    json_output: bool = typer.Option(False, "--json", help="Output as JSON"),
) -> None:
    """List all available strategies (built-in and user-defined)."""
    from clawquant.core.runtime.loader import list_strategies
    from clawquant.core.utils.output import print_result, print_table

    # Check global JSON mode from shared state
    from clawquant.core.utils.state import get_json_mode
    is_json = json_output or get_json_mode()

    strategies = list_strategies()

    if is_json:
        data = [s.model_dump(mode="json") for s in strategies]
        print_result(data, json_mode=True)
        return

    rows = [
        (s.name, s.version, s.description[:60], ", ".join(s.tags))
        for s in strategies
    ]
    print_table(
        headers=["Name", "Version", "Description", "Tags"],
        rows=rows,
        title="Available Strategies",
    )


