# validate
# ---------------------------------------------------------------------------
@strategy_app.command("validate")
def strategy_validate(
    name: Optional[str] = typer.Option(None, "--name", "-n", help="Strategy name to validate"),
    file: Optional[str] = typer.Option(None, "--file", "-f", help="Path to strategy .py file"),
) -> None:
    """Validate a strategy against the BaseStrategy interface."""
    from clawquant.core.runtime.loader import (
        discover_strategies,
        load_strategy,
        validate_strategy,
    )

    if name is None and file is None:
        typer.echo("Error: provide either --name or --file", err=True)
        raise typer.Exit(code=1)

    # Resolve strategy class
    if file is not None:
        filepath = Path(file)
        if not filepath.is_file():
            typer.echo(f"Error: file not found: {filepath}", err=True)
            raise typer.Exit(code=1)
        # Use loader's file: protocol to import
        from clawquant.core.runtime.loader import _import_module_from_file, _find_strategy_classes

        mod = _import_module_from_file(filepath)
        if mod is None:
            typer.echo(f"Error: failed to import {filepath}", err=True)
            raise typer.Exit(code=1)
        classes = _find_strategy_classes(mod)
        if not classes:
            typer.echo(f"Error: no BaseStrategy subclass found in {filepath}", err=True)
            raise typer.Exit(code=1)
        strategy_cls = classes[0]
    else:
        strategies = discover_strategies()
        if name not in strategies:
            available = ", ".join(sorted(strategies.keys())) or "(none)"
            typer.echo(f"Error: strategy '{name}' not found. Available: {available}", err=True)
            raise typer.Exit(code=1)
        strategy_cls = strategies[name]

    # Run validation
    result = validate_strategy(strategy_cls)

    # Display results
    try:
        cls_name = strategy_cls.metadata().name
    except Exception:
        cls_name = strategy_cls.__name__

    if result["valid"]:
        typer.echo(f"[PASS] Strategy '{cls_name}' passed all validation checks.")
    else:
        typer.echo(f"[FAIL] Strategy '{cls_name}' has validation errors:")

    for err in result["errors"]:
        typer.echo(f"  ERROR: {err}")
    for warn in result["warnings"]:
        typer.echo(f"  WARNING: {warn}")

    if not result["valid"]:
        raise typer.Exit(code=1)


